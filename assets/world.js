async function loadTrackedItems() {
  const candidatePaths = [
    "./data/market/items/tracked_items.json",
    "./data/market/tracked_items.json",
  ];

  for (const path of candidatePaths) {
    try {
      const response = await fetch(path);
      if (!response.ok) continue;

      const payload = await response.json();
      if (Array.isArray(payload)) return payload;
      if (Array.isArray(payload?.items)) return payload.items;
    } catch {
      // Try the next candidate path.
    }
  }

  return [];
}

function getOrderedTrackedItems(trackedItems) {
  const normalizedTrackedItems = Array.isArray(trackedItems)
    ? trackedItems.map((item) => String(item || "").trim()).filter(Boolean)
    : [];

  const trackedItemSet = new Set(normalizedTrackedItems);
  const orderedItems = MARKET_ITEM_ORDER.filter((itemName) =>
    trackedItemSet.has(itemName)
  );
  const remainingItems = normalizedTrackedItems.filter(
    (itemName) => !MARKET_ITEM_ORDER.includes(itemName)
  );

  return [...orderedItems, ...remainingItems];
}

function extractMarketEntries(payload) {
  if (Array.isArray(payload?.snapshots?.[0])) return payload.snapshots[0];
  if (Array.isArray(payload?.snapshots)) return payload.snapshots;
  if (Array.isArray(payload?.[0])) return payload[0];
  if (Array.isArray(payload)) return payload;
  return [];
}

