async function openMarketItemModal(worldName, itemName) {
  closeMarketItemModal();

  const modalRoot = document.createElement("div");
  modalRoot.id = "marketItemModalRoot";
  modalRoot.innerHTML = renderMarketItemModalShell(itemName);
  document.body.appendChild(modalRoot);
  document.body.style.overflow = "hidden";

  modalRoot.addEventListener("click", (event) => {
    if (!(event.target instanceof Element)) return;

    if (event.target.closest("[data-market-modal-close='true']")) {
      closeMarketItemModal();
      return;
    }

    if (event.target.matches("[data-market-modal-backdrop='true']")) {
      closeMarketItemModal();
    }
  });

  const onKeyDown = (event) => {
    if (event.key === "Escape") {
      closeMarketItemModal();
      document.removeEventListener("keydown", onKeyDown);
    }
  };
  document.addEventListener("keydown", onKeyDown);

  const chartsSection = modalRoot.querySelector(
    "[data-market-chart-section='supply-demand']"
  );
  const equilibriumSection = modalRoot.querySelector(
    "[data-market-chart-section='equilibrium']"
  );
  const metricsSection = modalRoot.querySelector("[data-market-metrics]");
  const dict = t();

  if (chartsSection) {
    chartsSection.innerHTML = `<p class="market-item-chart-empty">${escapeHtml(
      dict.loading
    )}</p>`;
  }
  if (equilibriumSection) {
    equilibriumSection.innerHTML = `<p class="market-item-chart-empty">${escapeHtml(
      dict.loading
    )}</p>`;
  }
  if (metricsSection) {
    metricsSection.innerHTML = `<p class="market-item-chart-empty">${escapeHtml(
      dict.loading
    )}</p>`;
  }

  try {
    const entries = await loadMarketEntries(worldName, itemName);
    const latest = getLatestMarketEntry(entries);
    const updatedLabel = modalRoot.querySelector(".market-item-modal-updated");
    const rangeButtons = Array.from(modalRoot.querySelectorAll("[data-range]"));

    if (updatedLabel) {
      updatedLabel.textContent = `${dict.updated}: ${
        latest ? formatMarketTimestamp(latest.time) : "N/A"
      }`;
    }

    requestAnimationFrame(() => {
      updateMarketItemModalContent(modalRoot, entries, "7D");

      rangeButtons.forEach((button, index) => {
        button.addEventListener("click", () => {
          const rangeKey = button.getAttribute("data-range") || "7D";
          requestAnimationFrame(() => {
            updateMarketItemModalContent(modalRoot, entries, rangeKey);
          });
        });

        button.addEventListener("keydown", (event) => {
          if (
            event.key !== "ArrowRight" &&
            event.key !== "ArrowLeft" &&
            event.key !== "Home" &&
            event.key !== "End"
          ) {
            return;
          }

          event.preventDefault();

          let targetIndex = index;

          if (event.key === "ArrowRight") {
            targetIndex = (index + 1) % rangeButtons.length;
          } else if (event.key === "ArrowLeft") {
            targetIndex = (index - 1 + rangeButtons.length) % rangeButtons.length;
          } else if (event.key === "Home") {
            targetIndex = 0;
          } else if (event.key === "End") {
            targetIndex = rangeButtons.length - 1;
          }

          rangeButtons[targetIndex]?.focus();
        });
      });
    });
  } catch (error) {
    console.error("Failed to open market item modal", error);

    if (chartsSection) {
      chartsSection.innerHTML = `<p class="market-item-chart-empty">${escapeHtml(
        dict.noMarketData
      )}</p>`;
    }
    if (equilibriumSection) {
      equilibriumSection.innerHTML = `<p class="market-item-chart-empty">${escapeHtml(
        dict.noMarketData
      )}</p>`;
    }
    if (metricsSection) {
      metricsSection.innerHTML = `<p class="market-item-chart-empty">${escapeHtml(
        dict.noMarketData
      )}</p>`;
    }
  }
}

function bindMarketPricesTableInteractions(cardElement, worldName) {
  const table = cardElement.querySelector(".market-prices-table");
  if (!table) return;

  table.addEventListener("click", (event) => {
    const row =
      event.target instanceof Element
        ? event.target.closest("[data-market-item-name]")
        : null;
    if (!row) return;
    const itemName = row.getAttribute("data-market-item-name");
    if (!itemName) return;
    openMarketItemModal(worldName, itemName);
  });

  table.addEventListener("keydown", (event) => {
    if (event.key !== "Enter" && event.key !== " ") return;
    const row =
      event.target instanceof Element
        ? event.target.closest("[data-market-item-name]")
        : null;
    if (!row) return;
    event.preventDefault();
    const itemName = row.getAttribute("data-market-item-name");
    if (!itemName) return;
    openMarketItemModal(worldName, itemName);
  });
}

async function renderMarketPrices(worldName) {
  const dict = t();
  const trackedItems = getOrderedTrackedItems(await loadTrackedItems());

  if (!Array.isArray(trackedItems) || trackedItems.length === 0) {
    return renderEmptyCard(dict.marketPrices, dict.noHistory);
  }

  const rows = await Promise.all(
    trackedItems.map(async (itemName) => {
      const marketFilePaths = buildMarketFilePath(worldName, itemName);

      try {
        let payload = null;

        for (const marketFilePath of marketFilePaths) {
          const response = await fetch(marketFilePath);
          if (!response.ok) continue;
          payload = await response.json();
          break;
        }

        if (!payload) {
          return {
            itemName,
            supply: "N/A",
            demand: "N/A",
            updated: "N/A",
          };
        }

        const entries = extractMarketEntries(payload);
        const latest = getLatestMarketEntry(entries);

        if (!latest) {
          return {
            itemName,
            supply: "N/A",
            demand: "N/A",
            updated: "N/A",
          };
        }

        return {
          itemName,
          supply: formatMarketValue(latest.day_average_sell),
          demand: formatMarketValue(latest.day_average_buy),
          updated: formatMarketTimestamp(latest.time),
        };
      } catch {
        return {
          itemName,
          supply: "N/A",
          demand: "N/A",
          updated: "N/A",
        };
      }
    })
  );

  const bodyRows = rows
    .map(
      (row) => `
        <tr data-market-item-name="${escapeHtml(row.itemName)}" tabindex="0">
          <td>${escapeHtml(row.itemName)}</td>
          <td>${escapeHtml(row.supply)}</td>
          <td>${escapeHtml(row.demand)}</td>
          <td>${escapeHtml(row.updated)}</td>
        </tr>
      `
    )
    .join("");

  return `
    <div class="world-detail-card-header">
      <h2>${escapeHtml(dict.marketPrices)}</h2>
    </div>
    <div class="world-history-table-wrap">
      <table class="world-history-table market-prices-table">
        <thead>
          <tr>
            <th>Item</th>
            <th>Supply Price</th>
            <th>Demand Price</th>
            <th>Updated</th>
          </tr>
        </thead>
        <tbody>${bodyRows}</tbody>
      </table>
    </div>
  `;
}

