function downloadMarketExport(itemName, rangeKey, rangeEntries, format) {
  const rows = Array.isArray(rangeEntries) ? rangeEntries : [];
  let content = "";
  let mimeType = "text/plain;charset=utf-8";
  let extension = format;

  if (format === "json") {
    mimeType = "application/json;charset=utf-8";
    content = JSON.stringify(rows, null, 2);
  } else {
    mimeType = "text/csv;charset=utf-8";
    extension = "csv";
    const headers = [
      "timestamp",
      "day_average_sell",
      "day_average_buy",
      "day_sold",
      "day_bought",
    ];
    const csvRows = rows.map((entry) =>
      [
        formatMarketTimestamp(entry?.time),
        entry?.day_average_sell ?? "",
        entry?.day_average_buy ?? "",
        entry?.day_sold ?? "",
        entry?.day_bought ?? "",
      ].join(",")
    );
    content = [headers.join(","), ...csvRows].join("\n");
  }

  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = `${slugifyItemName(itemName)}_${String(rangeKey).toLowerCase()}.${extension}`;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function getMarketEquilibriumValue(entry) {
  const supply = normalizeMarketMetricValue(entry?.day_average_sell);
  const demand = normalizeMarketMetricValue(entry?.day_average_buy);
  if (supply === null || demand === null) return null;
  return (supply + demand) / 2;
}

function getMarketDayKey(unixSeconds) {
  const numeric = Number(unixSeconds);
  if (!Number.isFinite(numeric) || numeric <= 0) return null;

  const date = new Date(numeric * 1000);
  const resolvedTimezone = resolveTimezoneValue(pageTimezone);
  const parts = new Intl.DateTimeFormat("en-CA", {
    timeZone: resolvedTimezone,
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).formatToParts(date);

  const partMap = Object.fromEntries(
    parts.map((part) => [part.type, part.value])
  );
  return `${partMap.year}-${partMap.month}-${partMap.day}`;
}

function getDailyMarketEntries(rangeEntries) {
  if (!Array.isArray(rangeEntries) || rangeEntries.length === 0) return [];

  const dailyEntryMap = new Map();

  for (const entry of rangeEntries) {
    const dayKey = getMarketDayKey(entry?.time);
    if (!dayKey) continue;

    const existingEntry = dailyEntryMap.get(dayKey);
    if (
      !existingEntry ||
      Number(entry?.time || 0) > Number(existingEntry?.time || 0)
    ) {
      dailyEntryMap.set(dayKey, entry);
    }
  }

  return Array.from(dailyEntryMap.values()).sort(
    (a, b) => Number(a?.time || 0) - Number(b?.time || 0)
  );
}

function averageMarketValues(entries, selector) {
  const values = entries
    .map((entry) => selector(entry))
    .filter((value) => value !== null);

  if (!values.length) return null;
  return values.reduce((sum, value) => sum + value, 0) / values.length;
}

function computeMarketMetrics(rangeContext) {
  const { rangeEntries } = rangeContext;
  const dailyEntries = getDailyMarketEntries(rangeEntries);

  if (!dailyEntries.length) {
    return {
      avgSupplyPrice: null,
      avgSupplyVolume: null,
      avgDemandPrice: null,
      avgDemandVolume: null,
      avgSpread: null,
      avgTransactions: null,
      dataPoints: 0,
    };
  }

  return {
    avgSupplyPrice: averageMarketValues(dailyEntries, (entry) =>
      normalizeMarketMetricValue(entry?.day_average_sell)
    ),
    avgSupplyVolume: averageMarketValues(dailyEntries, (entry) =>
      normalizeMarketMetricValue(entry?.day_sold)
    ),
    avgDemandPrice: averageMarketValues(dailyEntries, (entry) =>
      normalizeMarketMetricValue(entry?.day_average_buy)
    ),
    avgDemandVolume: averageMarketValues(dailyEntries, (entry) =>
      normalizeMarketMetricValue(entry?.day_bought)
    ),
    avgSpread: averageMarketValues(dailyEntries, (entry) => {
      const supply = normalizeMarketMetricValue(entry?.day_average_sell);
      const demand = normalizeMarketMetricValue(entry?.day_average_buy);
      if (supply === null || demand === null) return null;
      return supply - demand;
    }),
    avgTransactions: averageMarketValues(dailyEntries, (entry) => {
      const supplyVolume = normalizeMarketMetricValue(entry?.day_sold);
      const demandVolume = normalizeMarketMetricValue(entry?.day_bought);
      if (supplyVolume === null || demandVolume === null) return null;
      return (supplyVolume + demandVolume) / 2;
    }),
    dataPoints: dailyEntries.length,
  };
}

